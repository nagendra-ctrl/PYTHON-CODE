# Creating a tuple
my_tuple = (10, 20, 30, 40, 50, 60, 70, 80, 90)

# Getting the 4th element (index 3)
fourth_element = my_tuple[3]

# Getting the 4th element from the last (index -4)
fourth_from_last_element = my_tuple[-4]

# Printing the results
print("4th element:", fourth_element)
print("4th element from the last:", fourth_from_last_element)
